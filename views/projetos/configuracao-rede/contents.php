    <div class="ps-2 bloc">
        Desenvolvido para auxiliar os profissionais militares da seção de pagamento da Organização Militar (CPOR/SP), o programa foi criado em um contexto onde o Exército Brasileiro possuía diversas redes internas. Por questões de segurança, havia uma rede exclusiva para transações financeiras.
        <p class="p-1">
            Os responsáveis precisavam configurar manualmente a placa de rede com um endereço de IP, máscara de sub-rede, gateway, DNS preferencial e DNS alternativo específicos para realizar os pagamentos dos militares. Após a conclusão das transferências, era necessário reverter as configurações para as originais.
        </p>
        <p class="p-1">
            Para simplificar esse processo, o programa foi desenvolvido. Ele permite que as configurações de rede sejam ajustadas automaticamente. Após uma configuração inicial, o militar pode simplesmente executar o programa e selecionar a rede desejada, facilitando e agilizando o processo de pagamento.
